//把OLED_BMP_FONT中图片数据全部写入FLASH中  
//接在FLASH内部图片地址后面  返回0 成功  其它失败
// num为正确写入的照片数
u8  FLASH_Write_font(u16 *num)            
{
   u8   Error = 0;             //错误标记    0正常   1错误
   u16  i = 0;                 //扫描计数
   u8   CP_i=0 ;               //拷贝计数

   BMP_Code.bmp_num = FLASH_BMP_Scan();   //当前FLASH 中有的图片张数   同时也为下一张图片的索引
	
#if UART_TEST__EN
   Uart_printf("\r\n 起始图片索引为 %d \r\n\r\n",BMP_Code.bmp_num);
#endif
	
	 while((i<BMP_FONT_MAX)&&(BMP_Code.bmp_num<FLASH_Pointer_MAX)&&(BMP_Code.bmp_num<FLASH_BMP_NUBL))    //小于最大图片数组指针 个数 小于FLASH最大存储图片数量
	 {
     BMP_Code.bmp_aderr = OLED_CODE_Adress+1024*BMP_Code.bmp_num;    //图片数据开始地址
		  
		 BMP_Code.start = 0x5A;                      //起始标记
		 BMP_Code.end = 0xA5;                        //结束标记
     BMP_Code.MOS_flaot=0x1234 ;                 //大小端校验
		 BMP_Code.bmp_code = BMP_FONT[i].BMP_font;   //装载图片数据数组
		 BMP_Code.bmp_ID_code =BMP_FONT[i].BMP_ID;   //装载图片名字
		 
#if UART_TEST__EN
		 Uart_printf(" 图片名字为：%s \r\n",BMP_Code.bmp_ID_code);
#endif
		 
	//拷贝名字-------------------------------------------------   
		 for(CP_i=0;CP_i<BMP_ID_SIZE;CP_i++)  //最长为8个字节
		 {
       BMP_Code.bmp_ID[CP_i] = BMP_Code.bmp_ID_code[CP_i];    //拷贝名字
     }
		 
		 BMP_Code.bmp_ID[CP_i] = '\0';   //加入结束符
		 
//开始写入FLASH------------------------------------------------
    Error = FLASH_Write_bmp(&BMP_Code);
		 
		if(Error)
		{
#if UART_TEST__EN
			Uart_printf(" 错误返回值为 %d \r\n",Error);
#endif
       break;     //写入出错 则退出
    }
		
#if UART_TEST__EN
		Uart_printf(" 第 %d 张图片写入成功！ \r\n\r\n",i);	
#endif
		
    i++;    //正确则写下一个图片
		BMP_Code.bmp_num++;   //索引加一
   }
//---------------------------------------------------------- 
	 *num = i;     //保存写入图片的数量
	 
  if(i<BMP_FONT_MAX)   
	{
     return 1;      //没有全部写入   返回 1
  }
	else
	{
     return 0;    //已全部正常写入   返回 0
  }
}


