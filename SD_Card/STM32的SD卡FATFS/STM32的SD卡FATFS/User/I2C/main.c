int main(void)
{
	uint8_t WriteBuffer[256],ReadBuffer[256];//写/读数据的存储
	
	I2C_Configuration();//I2C的初始化/////////////

	/* 开始向EEPROM写数据 */
	I2C_Write(I2C1,ADDR_24LC02,0,WriteBuffer,sizeof(WriteBuffer) );//向I2C1写数据，24C02的地址，写入的字节地址，写入的数据，写入数据的字节数////////////////////////////

   	/* EEPROM读数据 */
	I2C_Read(I2C1,ADDR_24LC02,0,ReadBuffer,sizeof(WriteBuffer) );//从I2C1读数据，24C02的地址，读出的字节地址，读出的数据，读出数据的字节数

    while (1)
    {
    }
}

